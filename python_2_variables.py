# Вещественный тип данных
# Экспоненциальная форма записи числа
# 2.5e5 = 2,5*10 в 5 = 250000
# 1e-0.6 = 1*10 -6 
p = 0.1
# 10 в 5-ой степени
r = 2.5e5
# 10 в -6-ой степени
s = 1e-6
print(p, r, s)
# Комплексные числа
# j - символ мнимой единицы (0.1 - действительная часть 5j - мнимая часть
g = 0.1 + 5j
h = 1e2 - .3j
i = 4.5j
print(g, h, i)
