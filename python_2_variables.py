# как Python хранит и представляет различные данные?
# в Питоне нет переменных, в привычном смысле: когда переменная является именованным хранилищем данных
# x здесь имя ссылки, которая ссылается на "Hello World!". "Hello World!" представляется в Python как объект, содержащий эту строку
# Оператор присваивания не копирует данные в переменную, он лишь возвращает ссылку на объект с данными и эта ссылка сохраняется в переменной x.
# x - это ссылка (имена ссылок - кто что), которая хранит сообщение object = "Hello World!"
x = "Hello World!"
print(id(x))
x = 5
print(id(x))
print(type(x))
# Все переменные с одинаковой ссылкой id у всех одинаковый
a=b=c=0
print(id(a), id(b), id(c))
# Все переменные с разной ссылкой, id у всех разный
d, e, f = 1, 2, 3
print(id(d), id(e), id(f))
print(d, e, f)
# Обмен данными между двумя переменными, меняем местами две переменные
d, e = e, d
print(d, e)
# Встроенные типы данных НЕИЗМЕНЯЕМЫЕ None _ bool _ int _ float _ complex _ str
l = True
